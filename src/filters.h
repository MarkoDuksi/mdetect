    void downscale(const Image& src_image, Image& dst_image) {
        const float kernel_elements[] {
            1.0F, 1.0F, 1.0F, 1.0F,
            1.0F, 1.0F, 1.0F, 1.0F,
            1.0F, 1.0F, 1.0F, 1.0F,
            1.0F, 1.0F, 1.0F, 1.0F
        };
        const Kernel flat_kernel(kernel_elements, 4, 4, 0, 0);
        const uint8_t stride_X = 4;
        const uint8_t stride_Y = 4;
        auto postprocess = [](float x) { return static_cast<uint8_t>(x / 16); };

        convolve<float>(src_image, flat_kernel, stride_X, stride_Y, postprocess, dst_image);
    }

